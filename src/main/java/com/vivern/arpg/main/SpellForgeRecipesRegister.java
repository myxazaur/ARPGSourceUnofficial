   public static SpellForgeRecipe addRecipe(
      float hitsNeed,
      Ingridient craftresult,
      int Fire,
      int Earth,
      int Water,
      int Air,
      int Poison,
      int Cold,
      int Electric,
      int Void,
      int Pleasure,
      int Pain,
      int Death,
      int Live,
      @Nullable ShardType fallbackShardType,
      Ingridient slot0,
      Ingridient slot1,
      Ingridient slot2,
      Ingridient slot3,
      Ingridient slot4,
      Ingridient slot5,
      Ingridient slot6,
      Ingridient slot7,
      Ingridient slot8,
      Spell... spells
   ) {
      int[] array12elementsCost = new int[]{Fire, Earth, Water, Air, Poison, Cold, Electric, Void, Pleasure, Pain, Death, Live};
      if (slot0 != null
         && slot1 != null
         && slot2 != null
         && slot3 != null
         && slot4 != null
         && slot5 != null
         && slot6 != null
         && slot7 != null
         && slot8 != null
         && craftresult != null) {
         SpellForgeRecipe recipe = new SpellForgeRecipe(
            hitsNeed, new Ingridient[]{slot0, slot1, slot2, slot3, slot4, slot5, slot6, slot7, slot8}, craftresult, array12elementsCost
         );
         if (spells.length > 0) {
            recipe.setSpells(spells);
         }

         if (fallbackShardType != null) {
            recipe.fallbackShardType = fallbackShardType;
         }

         setregister(recipe);
         return recipe;
      } else {
         return new SpellForgeRecipe(hitsNeed, new Ingridient[]{EM, EM, EM, EM, EM, EM, EM, EM, EM}, EM, array12elementsCost);
      }
   }

