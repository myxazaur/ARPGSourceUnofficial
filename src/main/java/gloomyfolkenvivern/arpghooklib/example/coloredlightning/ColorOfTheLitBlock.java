package gloomyfolkenvivern.arpghooklib.example.coloredlightning;

public class ColorOfTheLitBlock {
   public int redSat;
   public int greenSat;
   public int blueSat;
   public int redPower;
   public int greenPower;
   public int bluePower;

   public ColorOfTheLitBlock(int redSat, int greenSat, int blueSat, int redPower, int greenPower, int bluePower) {
      this.redSat = redSat;
      this.greenSat = greenSat;
      this.blueSat = blueSat;
      this.redPower = redPower;
      this.greenPower = greenPower;
      this.bluePower = bluePower;
   }
}
